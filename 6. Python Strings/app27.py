# Some modifications to know:

# UpperCase: The upper() method returns the string in upper case:
a = "Hello, World!"
print(a.upper())


# LowerCase: The lower() method returns the string in lower case:
a = "Hello, World!"
print(a.lower())


# If you want to remove whitespace within the string, use the strip() method:
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"


# Replace String: The replace() method replaces a string with another string:
a = "Hello World"
print(a.replace("H","J"))


# Split Method: Returns a list where the text in between the specified separator becomes the list items
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']




