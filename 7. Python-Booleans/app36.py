# Booleans and functions go together pretty well too.

# You can create functions that returns a Boolean Value.

# EXAMPLE:
def myFunction() :
  return True

print(myFunction())


# ANOTHER EXAMPLE: Print "YES!" if the function returns True, otherwise print "NO!":
def myFunction() :
  return True

if myFunction():
  print("YES!")
else:
  print("NO!")