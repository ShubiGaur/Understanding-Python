# Outputs in Python are fairly simple, using print(). Here are some examples of it being implemented.

x = "Python is awesome"
print(x)

x = "Python"
y = "is"
z = "awesome"
print(x, y, z)

x = "Python "
y = "is "
z = "awesome"
print(x + y + z)

# For numbers, the + character works as a mathematical operator. 
# Combining a number with something else like a string will give you an error.

x = 5
y = 10
print(x + y)

# Best way to output multiple variables is to separate with commas
x = 5
y = "John"
print(x, y)