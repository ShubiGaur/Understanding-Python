# There are a lot of STRING methods!

x = "i love the Spurs"
capital = x.capitalize() # << Capitalizes the first letter of the string!
print(capital)

x = "The second string? Nice."
lower = x.casefold() # << Lower cases all the uppercase letters in a string
print(lower)

x = "center this thang"
cntr = x.center(30) # << Centers it (replace 30 with any custom amount)
print(cntr)

x = "count how many times Jane ate an apple at the apple festival near the apple tree"
text = x.count("apple") # << Returns the # of times the value "apple" appears in the string I made
print(text)

