# Sorting!

# Use sort() to list it a specific way

# You can sort alphabetically
thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.sort()
print(thislist)

# OR numerically
thislist = [100, 50, 65, 82, 23]
thislist.sort()
print(thislist)

# To sort in descending order
thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.sort(reverse = True) # << see the change
print(thislist)

