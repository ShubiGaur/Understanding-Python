# Inserting items in a list is simple. Just use insert()
thislist = ["apple", "banana", "cherry"]
thislist.insert(2, "watermelon") # << use insert(), 2 being the specific index
print(thislist)

# Use append() to add items to the END of a list:
thislist = ["apple", "banana", "cherry"]
thislist.append("orange")
print(thislist)

