# In programming...you often need to know if an expression is True or False.

# That's where booleans come in. Booleans represent 1 of 2 values: True or False.

# You can evaluate any expression in Python, and get one of two answers, True or False.

print(10 > 9)
print(10 == 9)
print(10 < 9)

# Run the file and see the outputs...either True or False!



