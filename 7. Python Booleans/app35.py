# The bool() function allows you to evaluate any value, and give you True or False in return,

# Evaluate a string and a number:
print(bool("Hello"))
print(bool(15))

x = "Hello"
y = 15

print(bool(x))
print(bool(y))

# Notice how they all ran true?

# Almost any value is evaluated to True if it has some sort of content.

# Any string is True, except empty strings.

# Any number is True, except 0.

# Try adjusting one of them above to 0 or an empty string. Run again and see what happens.

# It's nothing important to know. But just something to know for reference. 

# There are not many values that evaluate to False, except empty values, such as (), [], {}, "", the number 0, and the value None. 

