# Operators are used to perform operations on variables and values.

# ADDITION
print(10 + 5) # outputs 15

# SUBTRACTION
print(13 - 2) # outputs 11

# MULTIPLICATION
print(40 * 5) # outputs 200

# DIVISION
print(500 / 50) # outputs 10

# MODULUS
print(25 % 4) # outputs 1

# EXPONENTIATION
print(2 ** 4) # outputs 16

# FLOOR DIVISION (rounds the result down to the nearest whole number)
print(18 // 7) # outputs 2

