# Just like I mentioned earlier, you can get the data type of any object by using the type() function:

x = 5
print(type(x))

x = "Carmelo Anthony"
print(type(x))

x = 5.3
print(type(x))

x = 1j
print(type(x))

x = ["apple", "banana", "cherry"]
print(type(x))

x = ("apple", "banana", "cherry")	
print(type(x))

x = True	
print(type(x))

x = {"apple", "banana", "cherry"}	
print(type(x))

x = {"name" : "John", "age" : 36}	
print(type(x))
