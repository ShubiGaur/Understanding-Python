# Tuples: Another way to store multiple items in a single variable.

# A tuple is a collection which is ordered and UNCHANGEABLE.

# Tuples are written with round brackets.
thistuple = ("apple", "banana", "cherry")
print(thistuple)

# They are ordered, unchangeable, and allow duplicate values

# They are also indexed, like lists.

# Use len() in the same way to find the number of items in a list
thistuple = ("apple", "banana", "cherry")
print(len(thistuple))


